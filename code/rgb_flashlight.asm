    .include "m8def.inc"                ; We are using ATmega8

    .cseg                               ; Code segment
    .org $10                            ; No interrupts

.reset:

;==============================================================================
; Initialization
;==============================================================================

init:

    ; Set-up stack
    ldi r16, low(ramend)
    out spl, r16
    ldi r16, high(ramend)
    out sph, r16

;==============================================================================
; Main loop
;==============================================================================

main:

    rjmp main

